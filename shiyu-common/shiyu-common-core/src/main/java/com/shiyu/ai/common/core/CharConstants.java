package com.shiyu.ai.common.core;

/**
 * 常用字符/字符串常量工具类
 */
public class CharConstants {

    private CharConstants() {
        // 私有构造，防止实例化
    }

    // ========================= 标点符号 =========================
    public static final String COMMA = ",";           // 逗号
    public static final String PERIOD = ".";          // 句号
    public static final String SEMICOLON = ";";      // 分号
    public static final String COLON = ":";           // 冒号
    public static final String EXCLAMATION = "!";     // 感叹号
    public static final String QUESTION = "?";        // 问号
    public static final String DASH = "-";            // 破折号
    public static final String UNDERSCORE = "_";      // 下划线
    public static final String SLASH = "/";           // 斜杠
    public static final String BACKSLASH = "\\";      // 反斜杠
    public static final String PIPE = "|";            // 竖线
    public static final String AT = "@";              // @符号
    public static final String HASH = "#";            // #符号
    public static final String DOLLAR = "$";          // $符号
    public static final String PERCENT = "%";         // %符号
    public static final String AMPERSAND = "&";       // &符号
    public static final String ASTERISK = "*";        // *符号
    public static final String LEFT_PAREN = "(";      // 左括号
    public static final String RIGHT_PAREN = ")";     // 右括号
    public static final String LEFT_BRACE = "{";      // 左大括号
    public static final String RIGHT_BRACE = "}";     // 右大括号
    public static final String LEFT_BRACKET = "[";    // 左中括号
    public static final String RIGHT_BRACKET = "]";   // 右中括号
    public static final String QUOTE = "\"";          // 双引号
    public static final String SINGLE_QUOTE = "'";    // 单引号
    public static final String LESS_THAN = "<";       // 小于号
    public static final String GREATER_THAN = ">";    // 大于号
    public static final String TILDE = "~";           // 波浪号
    public static final String BACKTICK = "`";        // 反引号
    public static final String ELLIPSIS = "…";        // 省略号

    // ========================= 空白符 =========================
    public static final String SPACE = " ";           // 空格
    public static final String TAB = "\t";            // 制表符
    public static final String NEWLINE = "\n";        // 换行符
    public static final String CARRIAGE_RETURN = "\r";// 回车符

    // ========================= 字符编码 =========================
    public static final String UTF8 = "UTF-8";
    public static final String GBK = "GBK";

    // ========================= URL / 协议 =========================
    public static final String WWW = "www.";
    public static final String HTTP = "http://";
    public static final String HTTPS = "https://";

    // ========================= 通用标识 =========================
    public static final String SUCCESS = "0";
    public static final String FAIL = "1";
}
